// Definition for a binary tree node.
#[derive(Debug, PartialEq, Eq)]
pub struct TreeNode {
  pub val: i32,
  pub left: Option<Rc<RefCell<TreeNode>>>,
  pub right: Option<Rc<RefCell<TreeNode>>>,
}

impl TreeNode {
  #[inline]
  pub fn new(val: i32) -> Self {
    TreeNode {
      val,
      left: None,
      right: None
    }
  }
}
// @lc code=start
use std::rc::Rc;
use std::cell::RefCell;
impl Solution {
    pub fn invert_tree(root: Option<Rc<RefCell<TreeNode>>>) -> Option<Rc<RefCell<TreeNode>>> {
        let mut root_node = root.as_ref()?.borrow_mut();
        (root_node.left, root_node.right) = (root_node.right.take(), root_node.left.take());
        root_node.left = Self::invert_tree(root_node.left.take());
        root_node.right = Self::invert_tree(root_node.right.take());
        std::mem::drop(root_node);
        root   
    }
}
